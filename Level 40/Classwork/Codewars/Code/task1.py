# split
text = "porche,mercedes,BMW"
fruits = text.split(",")
print(fruits)

# join
words = ['Python', 'Html', 'Css']
sentence = ' '.join(words)
print(sentence)

# replace
text = "i am gio"
new_text = text.replace("gio", "luka")
print(new_text)

# strip
name = "   luke   "
clean_name = name.strip()
print(clean_name)

