 #სამივე ლოგიკურ ოპერატორზე: not, or, and: ჩამოწერეთ ყველა შესაძლო ვარიანტი და დაბეჭდეთ შედეგი გვერდზე კომენტარის საშვალებით მიწუერეთ შედეგი

# 'not' ოპერატორი
# # not True -> False
# not False -> True

# 'or' ოპერატორი
# True or False -> True
# False or True -> True
# False or False -> False

# 'and' ოპერატორი
# True and False -> False
# False and True -> False
# True and True -> True
# False and False -> False