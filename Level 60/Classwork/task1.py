class Account:
    _object_count = 0
# /////////////////////////////////////////////////////////////////////////////////
    def __init__(self, owner, balance):
        self._owner = owner 
        self.__balance = balance
        Account._object_count += 1
# /////////////////////////////////////////////////////////////////////////////////
    # protected 
    def get_owner(self):
        return self._owner
# /////////////////////////////////////////////////////////////////////////////////
    # private
    def get_balance(self):
        return self.__balance
# /////////////////////////////////////////////////////////////////////////////////
    @classmethod
    def get_object_count(cls):
        return cls._object_count
# /////////////////////////////////////////////////////////////////////////////////
# კლასის ატრიბუტი — გამოიყენება ობიექტების საერთო მონაცემების შესანახად
# Protected ატრიბუტი — "_"
# Private ატრიბუტი — "__"
