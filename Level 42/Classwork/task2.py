# Dictionary არის მონაცემთა სტრუქტურა, რომელიც ინახავს მონაცემებს (key-value) წყვილებად.

me = {
    "name": "luka",
    "hobby": "coding",
    "height": 175,
    "weight": 67
}
'/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////'
print(me.get("name"))      
print(me.get("age", "No age"))
'/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////'
print(me.items())
'/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////'
print(me.keys())
'/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////'
person_copy = me.copy()
print(person_copy)
'/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////'
height = me.pop("height")
print(height)   
print(me)       
'/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////'
last_item = me.popitem()
print(last_item)  
print(me)        
'/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////'
me.update({"age": 25, "hobby": "riding"})
print(me)
'/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////'
me.clear()
print(me)
