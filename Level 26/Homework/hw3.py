# 4) ახსენით კომენტარებით როგორ მუშაობს: lower, upper, capitalize მეთოდები

# lower()
# ეს მეთოდი ყველა სიმბოლოს პატარა ასოდ წერს
text = "Hello World"
print(text.lower())  # დაბეჭდავს: "hello world"

# upper()
# ეს მეთოდი ყველა სიმბოლოს დიდ ასოდ დაწერს
text = "Hello World"
print(text.upper())  # დაბეჭდავს: "HELLO WORLD"

# capitalize() 
# ეს მეთოდი მხოლოდ პირველი სიმბოლოს დიდად დაწერს, ხოლო დანარჩენებს პატარა ასოდ დაწერს
text = "hello world"
print(text.capitalize())  # დაბეჭდავს: "Hello world"
