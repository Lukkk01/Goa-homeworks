let myMap = new Map([]);
myMap.set("name", "Nika");
myMap.set("age", 25);
myMap.set("country", "Georgia");
myMap.set("isStudent", false);
myMap.set("score", 98);
///////////////////////////////////////////////////////////////////////////////////////
// get(key) — აბრუნებს მნიშვნელობას
console.log(myMap.get("name"))
///////////////////////////////////////////////////////////////////////////////////////
// has(key) — ამოწმებს არსებობს თუ არა key
console.log(myMap.has("age"));
///////////////////////////////////////////////////////////////////////////////////////
// delete(key) — შლის ელემენტს key-ის მიხედვით
myMap.delete("isStudent");
///////////////////////////////////////////////////////////////////////////////////////
// keys() — აბრუნებს ყველა key-ს
console.log([...myMap.keys()]);
///////////////////////////////////////////////////////////////////////////////////////
// values() — აბრუნებს ყველა value-ს
console.log([...myMap.values()]);
///////////////////////////////////////////////////////////////////////////////////////
// entries() — აბრუნებს [key, value] წყვილებს
console.log([...myMap.entries()]);
